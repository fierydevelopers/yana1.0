<script type="text/javascript">
    $(function() {
        let step_one;
        let current_step;
        let allowAnim = false;
        let steps = $('.step');
        steps.hide();

        $.Velocity.RegisterEffect("transition.stepAnim", {
            defaultDuration: 1000,
            calls: [
                [{
                    r: '5',
                    fill: '#138D75',
                    stroke: '#0E6655',
                    strokeWidth: '3px'
                }, 0.5, {
                    easing: [10, 5]
                }],
                [{
                    r: '15'
                }, 0.5, {
                    easing: [70, 8]
                }]
            ]

        })
            .RegisterEffect("transition.inverseStepAnim", {
                defaultDuration: 500,
                calls: [
                    [{
                        r: '20',
                        fill: '#138D75',
                        stroke: '#ffffff',
                        strokeWidth: '12px'
                    }, 1, {
                        easing: [10, 5]
                    }]
                ]

            });

        function init() {
            steps
                .velocity('transition.bounceLeftIn', {
                    duration: 1000,
                    stagger: 70
                })
                .velocity({
                    strokeWidth: [12, 0]
                }, {
                    duration: 1000,
                    easing: [70, 8],
                    complete: function() {
                        allowAnim = true;
                    }
                });
            current_step = $('.current-step');
            step_one = $('#step1');

            changeStep(step_one);

        }

        init();

        steps.click(function(e) {
            if (allowAnim) {
                changeStep($(this));
            }
        });

        function changeStep(element) {
            // $('.step-info').hide();
            let cur_pos = element.attr('cx');

            $('.cursor').velocity({
                cx: cur_pos
            }, {
                duration: 500,
                easing: [70, 8]
            });

            let num = parseInt(cur_pos) / 14;

            $('.step-number').text(num);

            $('.step-number').velocity({
                left: cur_pos
            }, {
                duration: 500,
                easing: [70, 8]
            });
            $('.step-info:nth-of-type('+( num + 1 )+')').velocity('transition.slideUpIn', {delay : 1000});
            if (element.attr('class') == 'step') {
                current_step.velocity('transition.inverseStepAnim');
                current_step.attr("class", "step");
            }
            element.velocity('transition.stepAnim');
            element.attr("class", "step current-step");
            current_step = element;
        };

    });
</script>
